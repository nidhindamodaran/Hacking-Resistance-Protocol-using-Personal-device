private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
    if (homeDir == null) {
        JOptionPane.showMessageDialog(this, "Please provide home directory",
                "Invalid home", JOptionPane.OK_OPTION);
    } else {
        LinkedHashMap<String, FileInfoBean> fileInfos = AppVariables.settingsInfo.getFileInfos();
        DecryptDirectory decryptDirectory = new DecryptDirectory(fileInfos);
        new Thread(decryptDirectory).start();
        AppVariables.settingsInfo.setFileInfos(new LinkedHashMap<String, FileInfoBean>());
        AppVariables.settingsInfo.setEncrHomeDir(homeDir);
        deataMain.loadTree();
        this.dispose();
    }
}//GEN-LAST:event_okButtonActionPerformed

