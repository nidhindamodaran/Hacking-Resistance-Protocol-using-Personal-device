private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed


    JFileChooser chooser = new JFileChooser() {

        @Override
        public void approveSelection() {
            File seFile = getSelectedFile();
            if (seFile.isDirectory() && seFile.exists()) {
                super.approveSelection();
            } else {
                JOptionPane.showMessageDialog(this, "The selected location does not exists", CANCEL_SELECTION, WIDTH);
            }
        }
    };
    chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
    if (chooser.showDialog(this, "Select Home Directory")
            == JFileChooser.APPROVE_OPTION) {
        homeDir = chooser.getSelectedFile();
        homeDirTextField.setText(homeDir.getAbsolutePath());
    }

}//GEN-LAST:event_browseButtonActionPerformed

